def build_extract_elements_prompt(
    genre: str,
    logline: str, characters: str, structure: str,
    scene_design: str, treatment: str, tone: str, world: str,
) -> str:
    """기획 자료에서 시나리오 집필에 필수적인 핵심 요소를 추출한다.
    이 결과는 매 비트 집필 시 강제 주입된다."""
    return f"""
[TASK] 핵심 요소 추출 — Story Elements Extraction

아래 기획 자료를 분석하여 시나리오 집필에 반드시 반영해야 할 핵심 요소를 추출하라.
이 추출 결과는 매 비트 집필 시 강제로 주입되어, 누락을 방지한다.

[기획 자료]
[LOGLINE] {logline}
[캐릭터 + 바이블] {characters[:4000]}
[구조] {structure[:2000]}
[장면설계] {scene_design[:2000]}
[트리트먼트] {treatment[:3000]}
[톤 문서] {tone[:1500]}
[세계관] {world[:1000]}

[추출 항목 — 아래 형식으로 정확히 출력하라]

===액션 아이디어 (Action Idea)===
이 영화 전체를 하나의 행동(Action)으로 요약하라.
- 액션 아이디어: "주인공이 ___하는 일" (한 문장. 행동 동사 필수.)
- 이 행동이 완수되면 관객이 느끼는 카타르시스: ___
- 이 행동을 방해하는 핵심 장애물: ___
- 이 행동이 시작되는 비트: Beat ___
- 이 행동이 완수되는 비트: Beat ___
(아리스토텔레스: 행동이 인물보다 중요하다. 이 영화의 모든 비트는 이 행동을 향해 가거나, 이 행동을 방해한다.)

===서사동력 (Narrative Drive)===
주인공의 욕망이 어디서 시작되었는가? 이것이 비트별 감정 방향을 결정한다.

- 발생요인: 상실(Loss) / 결핍(Lack) (택 1)
  · 상실 = "한때 가졌던 것을 잃었다" → 회복/복수/대체 방향
  · 결핍 = "처음부터 없었다" → 획득/성장/증명 방향

- Goal (외적 욕망): ___ (주인공이 말로 표현할 수 있는 목표)
- Need (내적 필요): ___ (주인공이 인식하지 못하는 진짜 필요)
- Goal과 Need의 간극: ___ (이 간극이 이야기의 엔진이다)

- 서사 구조 방향:
  · 1막: ___ (상실이면 "잃은 직후/진실 발견", 결핍이면 "기회 발견")
  · 미드포인트: ___ (상실이면 "되찾을 수 없음 인식", 결핍이면 "대가가 예상보다 큼")
  · All Is Lost: ___ (상실이면 "자기 탓 인식", 결핍이면 "획득해도 안 채워짐")
  · 클라이맥스: ___ (상실이면 "집착 vs 수용", 결핍이면 "욕망 포기 vs 진짜 필요 선택")

- 해결전략: ___ (이 이야기만의 독특한 답. 테마의 구현.)
(기획 자료의 GNS + 캐릭터 바이블 backstory + 구조에서 추출)

===맥거핀===
- 맥거핀 1: (이름) — (역할) — (처음 등장 비트) — (최종 역할)
- 맥거핀 2: ...
(맥거핀 = 플롯을 움직이는 핵심 소도구/정보/장소. 없으면 "없음")

===캐릭터 비밀===
- (인물명)의 비밀: (내용) — 폭로 시점: (비트/에피소드) — 폭로 시 영향: (누구에게 어떤 변화)
- ...
(기획 자료의 캐릭터 바이블에서 secret 항목을 모두 추출)

===캐릭터 전술===
- (인물명): 전술1 / 전술2 / 전술3
- ...
(기획 자료의 캐릭터 바이블에서 tactics 항목을 모두 추출)

===핵심 장소===
- (장소명): (역할) — (감정적 의미) — (반복 등장 규칙)
- ...
(톤 문서의 recurring_locations + 세계관에서 추출)

===모티프===
- (모티프명): (등장 규칙)
- ...
(톤 문서의 motifs에서 추출. 예: "물 — 모든 씬에서 물이 어디에 있는지 확인")

===클라이맥스 설계===
- 클라이맥스 핵심 행동 시퀀스: (단계별 행동)
- 장르적 쾌감 포인트: (관객이 느낄 것)
- 테마 증명 행동: (주인공의 어떤 행동이 테마를 증명하는가)
(구조/장면설계/트리트먼트에서 클라이맥스 관련 내용 추출)

===톤 하드 룰===
- (톤 문서의 writer_instruction에서 추출한 절대 규칙들)
- ...

===Planting & Payoff (심기와 회수)===
1막에 심은 것이 3막에서 새로운 의미로 회수되어야 한다.
기획 자료(Core Build/Scene Design/Treatment)에서 설계된 Plant-Payoff 쌍을 추출하라.
없으면 아래 3가지를 직접 설계하라.

- ① 캐릭터 Plant: (주인공의 습관/대사/소품) → 심는 비트: Beat ___ → 회수 비트: Beat ___
- ② 관계 Plant: (인물 간 사소한 상호작용) → 심는 비트: Beat ___ → 회수 비트: Beat ___
- ③ 세계관 Plant: (세계의 규칙/장소/사물) → 심는 비트: Beat ___ → 회수 비트: Beat ___

규칙:
- 좋은 Plant: 관객이 심어진 줄 모른다. Payoff 순간에 비로소 깨닫는다.
- Plant 없이 등장하는 Payoff는 데우스 엑스 마키나다. 금지.
- 회수되지 않는 Plant는 관객의 불만족을 만든다.
(기획 자료의 planting_payoff / plant_payoff_tag / plant_payoff 필드에서 추출)

===장르 드라이브 설계===

[정보 비대칭 맵]
- 관객이 먼저 아는 정보 (Dramatic Irony): (내용) → 공개 비트 → 효과
- 관객도 모르는 정보 (Mystery Box): (내용) → 공개 비트 → 효과
- 주인공만 아는 정보: (내용) → 공개 비트
- 정보 공개 스케줄: Beat __에서 A 공개, Beat __에서 B 공개...
(기획 자료의 캐릭터 비밀 + 구조에서 추출. 비밀이 "언제, 누구에게" 드러나는지.)

[에스컬레이션 곡선]
- 1막(Beat 1~5) 위협: (구체적으로 무엇이 위협받는가)
- 2막 전반(Beat 6~8) 위협: (↑ 커져야 함)
- 미드포인트(Beat 8~9) 위협: (게임 체인저)
- 2막 후반(Beat 10~11) 위협: (↑↑ 더 커져야 함)
- 3막(Beat 12~15) 위협: (최대)
- 주인공이 각 구간에서 잃는 것: (구체적으로)

[적대자 위협 실체 — Villain 4 Questions]
① 흥미로운가?
- 적대자의 구체적 목표: ___ (뻔한 "세계 정복"이면 안 됨)
- 적대자의 독특한 동기/배경/성격: ___ (클리셰를 피하는 구체적 요소)
- 적대자의 능력/자원: ___ (왜 주인공이 쉽게 이길 수 없는가)

② 주인공의 다크 미러인가?
- 적대자가 주인공의 어떤 면을 반영하는가: ___ ("주인공이 다른 선택을 했다면" 이 사람이 된다)
- 주인공의 아크와 테마적 연결: ___ (적대자의 존재가 주인공의 변화를 촉발하는가)
- 주인공이 적대자를 보며 두려워하는 것: ___ (내가 저렇게 될 수 있다는 공포)

③ 등장 시 주인공의 계획을 뒤엎는가?
- 적대자가 등장하면 주인공의 현재 계획이 완전히 무너져야 한다
- 적대자 등장이 단순히 액션 시퀀스를 시작하는 것이면 안 됨 — 플롯이 바뀌어야 함
- 적대자가 성공하면 파괴되는 것: ___ (추상어 금지. 구체적으로.)

④ 빌런의 승률 설계 (가장 중요)
- Beat 1~5: 적대자 ___ 승 / 주인공 ___ 승
- Beat 6~11: 적대자 ___ 승 / 주인공 ___ 승
- Beat 12~15: 적대자 ___ 승 / 주인공 ___ 승
- 원칙: 적대자가 거의 모든 대결에서 이겨야 한다. 주인공이 마지막에만 이긴다.
  (Patriot Games 규칙: 빌런이 매번 실패하면 클라이맥스에서 아무도 긴장하지 않는다.)
- 적대자의 타이머/데드라인: ___ (시간 압박이 있는가?)

[장르 쾌감 포인트]
- 이 장르에서 관객이 느껴야 할 핵심 감정 3가지: ___
- 그 감정이 가장 강하게 폭발하는 비트: Beat ___
- 장르 셋피스 위치: Beat ___에서 ___ 장면
- "손에 땀을 쥐는 순간" 최소 3개: Beat ___ / Beat ___ / Beat ___

각 항목은 기획 자료에서 직접 추출. 없는 항목은 "기획 자료에 없음 — 집필 시 설계 필요"로 표기.
""".strip()


# ═══════════════════════════════════════════════════════════
# 2. WRITE BEAT — 비트 단위 집필
# ═══════════════════════════════════════════════════════════

def build_write_beat_prompt(
    genre: str,
    beat_number: int,
    scene_plan: str,
    characters: str,
    treatment: str,
    tone: str,
    previous_scene_text: str = "",
    logline: str = "",
    world: str = "",
    story_elements: str = "",
) -> str:
    gr = _genre_text(genre)
    genre_override = get_genre_override(genre)
    beat_info = BEATS_15[beat_number - 1] if 1 <= beat_number <= 15 else {}

    # 캐릭터 바이블 — 매번 전문 (최대 4000자)
    char_block = characters[:4000] if characters else "(캐릭터 정보 없음)"
    # 톤 — 매번 포함
    tone_block = tone[:1500] if tone else ""
    # 트리트먼트 — 전문 (AI가 해당 비트 구간 참조)
    treat_block = treatment[:5000] if treatment else ""

    # 직전 비트 연속성
    prev_block = ""
    if previous_scene_text:
        prev_block = f"\n[직전 비트 마지막 부분 — 연속성 유지]\n{previous_scene_text[-2500:]}\n"

    # Beat 1 = 오프닝 특별 지시
    opening_block = ""
    if beat_number == 1:
        genre_data = GENRE_RULES.get(genre, {})
        opening_strategy = genre_data.get("opening", "")
        opening_block = f"""
[⚡ 오프닝 특별 지시 — Beat 1 전용]
이것은 관객이 가장 먼저 보는 장면이다. 첫 3분 안에 관객을 잡아야 한다.

오프닝 전략: {opening_strategy}

[오프닝 규칙]
- 첫 씬의 첫 3줄이 관객의 호기심을 잡아야 한다. 설명으로 시작하지 마라.
- 첫 장면에서 작품 전체의 테마를 깔아라.
- 가능하면 대화의 중간, 또는 사건의 중간에서 시작하라 (Drop in the Middle).
- 첫 씬이 끝났을 때 관객이 "다음에 무슨 일이 벌어지지?"라고 물어야 한다.
- 주인공의 일상을 길게 보여주지 마라. 일상 속 균열을 보여줘라.
"""

    # Beat 1~5 = 장르 대기 (Genre Atmosphere) — 초반이 지루해지는 것을 방지
    atmosphere_block = ""
    if beat_number <= 5:
        genre_data = GENRE_RULES.get(genre, {})
        genre_en = genre_data.get("en", genre)
        hooks = genre_data.get("hooks", "")
        atmosphere_block = f"""
[🌡️ 장르 대기(Genre Atmosphere) — Beat 1~5 초반 지루함 방지]
사건이 본격적으로 시작되기 전(Beat 1~5)이라도 장르의 분위기는 첫 줄부터 작동해야 한다.
"일상을 보여주는 씬"과 "장르 분위기가 깔린 일상 씬"은 완전히 다른 것이다.

장르 ({genre_en}) 초반 분위기 규칙:
- 모든 지문에 장르의 감각이 묻어 있어야 한다. "평범하게 보이지만 뭔가 이상한" 디테일.
- 씬 시작의 Hook: {hooks}
- 인물의 행동, 공간의 묘사, 소리와 빛 — 모두 장르의 톤이어야 한다.

❌ 장르 대기 없는 초반 (지루한 이유):
  "지훈이 부엌에서 라면을 끓인다. 물이 끓는다. 라면을 넣는다."
  → 이건 호러가 아니라 일상 브이로그다.

✅ 장르 대기 있는 초반:
  "지훈이 부엌에서 라면을 끓인다. 냄비 뚜껑에 수증기. 창문에 물방울이 맺힌다.
   싱크대 배수구에서 꺼억 소리. 지훈이 고개를 돌린다. 멈춘다. 아무것도 없다."
  → 같은 라면 씬인데 관객은 불안하다. 이것이 장르 대기다.

★ Beat 1~5의 모든 씬에서 자가 점검:
  "이 씬에서 장르를 제거하면 다른 장르 영화에 넣어도 되는가?"
  → Yes면 장르 대기가 없는 것이다. 장르의 감각을 씬 곳곳에 심어라.
"""

    return f"""
[TASK] Beat {beat_number} 시나리오 집필 — {beat_info.get('name', '')} ({beat_info.get('act', '')})
{beat_info.get('desc', '')}

이 비트에 해당하는 모든 씬을 한국 표준 시나리오 서식으로 집필하라.

[장르]
{gr}
{genre_override}
[로그라인] {logline or '(씬 플랜 참조)'}
{opening_block}
{atmosphere_block}
[씬 플랜 — 이 비트의 씬을 찾아 정확히 따르라]
{scene_plan}

[캐릭터 바이블 — 각 인물의 말투·리듬·태도를 반드시 반영]
{char_block}

[세계관]
{world[:1500] if world else '(씬 플랜 참조)'}

[트리트먼트 — Beat {beat_number}에 해당하는 내용 참조]
{treat_block}
(트리트먼트에 villain_beat 항목이 있으면: 이 비트에서 적대자가 한 행동 + 승/패를 반드시 시나리오에 반영하라.)

{f"[톤 문서]{chr(10)}{tone_block}" if tone_block else ""}
{prev_block}

{f"[⚡ 핵심 요소 — 반드시 반영. 누락 시 실패.]{chr(10)}{story_elements}" if story_elements else ""}

[RULES — 핵심 요소 강제]
0. 위 [핵심 요소]의 맥거핀, 캐릭터 비밀, 핵심 장소, 모티프가 이 비트에 해당되면 반드시 씬에 등장시켜라.
   - 맥거핀: 이 비트에서 맥거핀이 어떻게 관여하는가? 관여하지 않으면 왜 안 하는지 내부 메모에 기록.
   - 캐릭터 비밀: 이 비트에서 폭로되는 비밀? 유지되는 비밀? 비밀을 아는 캐릭터와 모르는 캐릭터는?
   - 핵심 장소: 씬 플랜의 장소가 핵심 장소 목록에 있으면, 추출된 감정적 의미·규칙을 반영하라.
   - 모티프: 매 씬에서 모티프가 어디에 있는지 확인하라. 없으면 의도적 부재인지 점검.
   - 캐릭터 전술: 이 비트에서 각 캐릭터가 사용하는 전술이 바이블의 전술 목록과 일치하는지 확인.
   - Planting & Payoff: 핵심 요소의 Plant-Payoff 설계를 확인하라.
     · 이 비트가 plant(심기) 비트이면: 자연스럽게 심어라. 관객이 눈치채면 안 된다.
     · 이 비트가 payoff(회수) 비트이면: 심었던 것을 새로운 의미로 회수하라. 관객이 '아!' 하는 순간.
     · 트리트먼트에 plant_payoff 필드가 있으면 반드시 따르라.

[RULES — 장르 드라이브 (장르적 재미의 핵심)]
0-1. 이 비트를 집필하기 전에 아래 5개 질문에 먼저 답하라. 답이 씬에 반영되어야 한다.

  ① 정보 비대칭: 지금 관객이 아는데 주인공이 모르는 것은 무엇인가?
     → 이 비대칭이 이 비트의 씬에서 긴장을 만들고 있는가?
     → 이 비트에서 새로 공개되는 정보는? 새로 숨겨지는 정보는?

  ② 에스컬레이션: 이 비트의 위협은 직전 비트보다 구체적으로 얼마나 커졌는가?
     → 주인공이 이 비트에서 잃는 것(관계/자원/시간/안전)은 무엇인가?
     → 위협이 커지지 않았다면, 이 비트는 장르적으로 정체 상태다. 올려라.

  ③ 적대자 — 빌런이 이기고 있는가?
     → 이 비트에서 적대자가 구체적으로 무엇을 했는가? ("위협적이다"가 아니라 "무엇을 했다")
     → 적대자가 이 비트에서 이겼는가, 졌는가? (원칙: 클라이맥스 전까지 적대자가 계속 이긴다)
     → 적대자의 등장이 주인공의 현재 계획을 뒤엎었는가? (단순 액션이 아니라 플롯 변경)
     → 적대자가 이 비트에 직접 등장하지 않더라도, 그의 영향(이전 승리의 여파)이 느껴져야 한다.
     → 주인공이 적대자를 보며 "내가 저렇게 될 수 있다"는 공포를 느끼는가? (다크 미러)
     ★ 빌런이 매번 실패하면 클라이맥스에서 아무도 긴장하지 않는다. (Patriot Games 규칙)

  ④ 타이머/데드라인: 남은 시간·기회·자원이 줄었는가?
     → 관객이 "시간이 없다"를 체감하는가? 추상적 긴박이 아니라 구체적 카운트다운.
     → 타이머가 0이 되면 구체적으로 무엇이 파괴되는가? (관객이 상상할 수 있어야 한다)

  ⑤ 장르 쾌감: 이 비트에서 관객이 "손에 땀을 쥐는" / "가슴이 뛰는" / "소름 돋는" 순간이 최소 1개 있는가?
     → 없으면 이 비트는 장르적으로 실패다. 장르 장치를 더 강하게 걸어라.

  위 5개 중 3개 이상이 "아니오"면 이 비트는 캐릭터 드라마에 장르가 종속된 상태다.
  장르 드라이브를 올려라.

[RULES — 액션 아이디어 검증]
0-2. 이 비트는 액션 아이디어(핵심 요소의 "주인공이 ___하는 일")를 향해 전진하고 있는가?
   - 전진: 주인공이 그 행동에 한 걸음 가까워졌다 → OK
   - 방해: 무언가가 그 행동을 막았다 → OK (갈등)
   - 무관: 이 비트가 그 행동과 아무 상관이 없다 → 이 비트는 존재 이유가 없다. 연결하라.
   (아리스토텔레스: 모든 비트는 하나의 행동을 향한다. Unified Plot Test와 동일한 원칙.)

[RULES — 서사동력 체크 (Narrative Drive)]
0-3. 이 비트에서 주인공의 Goal(외적 욕망)과 Need(내적 필요)의 간극이 유지되고 있는가?
   - Goal을 향한 전진이 있는가? (있어야 함 — 주인공은 Goal을 계속 쫓는다)
   - 동시에 Need와는 멀어지고 있는가? (1~2막에서는 멀어져야 긴장이 생긴다)
   - 미드포인트 이후: Goal 추구가 좌절되면서 Need를 인식하기 시작하는가?
   - 클라이맥스: Goal을 포기하고 Need를 선택하는가, 또는 Goal과 Need가 합치되는가?
   ※ 핵심 요소의 서사동력(상실/결핍)에 따라 이 비트의 감정 방향이 결정된다.
      상실 기반이면: 되찾으려는 시도 → 되찾을 수 없음 → 수용/파멸
      결핍 기반이면: 쟁취하려는 시도 → 대가 인식 → 진짜 필요한 것 발견

[RULES — 비트 구조 변주 (2막 반복 패턴 방지)]
0-4. 비트의 내부 구조가 직전 비트와 같으면 안 된다.
   6가지 비트 구조 유형 중 이 비트에 가장 맞는 것을 택하되, 직전 비트와 반드시 다른 유형을 써라.

   [INV] 조사/발견형 — 단서 수집, 정보 조각, 진실에 접근
   [CON] 대결/충돌형 — 직접 대면, 언쟁, 물리적 충돌, 갈등 폭발
   [REV] 반전/배신형 — 예상 뒤집기, 동맹 붕괴, 새로운 진실
   [EMO] 감정/관계형 — 감정 씬 중심, 관계 변화, 고백, 결별
   [ACT] 행동/추격형 — 물리적 행동, 시간 압박, 도주, 구출
   [SIL] 정적/결심형 — 침묵, 내면, 결심의 순간, 폭풍 전 고요

   ❌ 2막 반복 패턴:
   Beat 6 [INV] → Beat 7 [INV] → Beat 8 [INV] → Beat 9 [INV]
   = 매 비트가 "조사 → 단서 → 대화 → 클리프" 반복

   ✅ 2막 변주:
   Beat 6 [INV] → Beat 7 [CON] → Beat 8 [REV] → Beat 9 [EMO] → Beat 10 [ACT] → Beat 11 [SIL]
   = 매 비트가 다른 감정/구조/리듬

   ★ 직전 비트의 내부 메모에 "비트 구조 유형"이 있으면 반드시 확인하고 다른 유형을 선택하라.
   ★ 특히 2막(Beat 6~11)에서 같은 유형이 2회 연속이면 무조건 경고.

[RULES]
1. 씬 플랜에서 Beat {beat_number}의 씬들을 찾아 전부 집필.

[분량 기준 — 반드시 준수]
2. 1씬 = 600~800자(한국어). 200자 미만 씬은 너무 짧다. 반드시 600자 이상 쓸 것.
   단, [BR] 호흡 씬은 200~400자도 허용. 짧지만 이미지가 강해야 한다.
3. 비트당 총 4,000~5,000자 이상. 이 분량에 미달하면 씬이 부족하거나 씬이 너무 얇은 것이다.
4. 지문 3~4줄 + 대사 2~4개 교환 + turn + exit hook = 1씬의 최소 구성.
5. 대사 없이 지문만으로 끝나는 씬은 1~2개까지만 허용. 나머지는 반드시 대사를 포함.

[씬 다양성 — 지루하지 않은 100씬의 핵심]
5-1. 씬 플랜의 씬 타입([A]/[B]/[BR]/[CUT])을 반드시 따르라:
   [A] 메인 사건 씬 — 플롯 전진. 갈등과 전환이 있어야.
   [B] 서브플롯 씬 — 관계·감정·테마. 메인과 다른 감정 톤.
   [BR] 호흡 씬 — 짧다(200~400자도 OK). 캐릭터의 인간적 순간. 대사 0~1개.
        예: 커피를 마시며 창밖을 보는 주인공. 무전기를 내려놓는 손. 빈 복도의 발소리.
   [CUT] 컷어웨이 씬 — 다른 인물의 시점. 동시다발. 리듬 전환.
        예: 주인공이 위기인 동안 적대자가 다른 곳에서 다음 수를 두는 장면.

5-2. 씬 리듬 변주:
   - 긴 대화 씬(2~3분) 뒤에는 짧은 행동/이미지 씬(30초)을 넣어라.
   - 긴장 씬 뒤에는 이완 씬. 이완 씬 뒤에는 더 큰 긴장.
   - 같은 감정 톤의 씬이 3개 연속이면 안 된다.

5-3. 장소 분산:
   - 같은 장소에서 연속 3씬 금지. 반드시 장소를 전환하라.
   - 같은 장소를 2분 간격으로 쪼개서 별도 씬으로 만들지 마라 — 하나의 씬으로 합쳐라.
   - ★ 장소와 시간이 바뀌지 않으면 새 씬 번호(S#)를 부여하지 마라. 같은 장소의 연속 행동은 하나의 씬이다.

[장르 강제 적용]
6. 이 비트의 모든 씬에서 장르 필수 장치(위 10개) 중 최소 2개가 작동해야 한다.
7. 비트 전체에서 장르 장치 5개 이상이 등장해야 한다.
8. 장르의 Hook으로 씬을 시작하고, 장르의 Punch로 씬을 끝내라.
9. 장르 실패 신호가 느껴지면 그 씬은 다시 써야 한다.

[서브플롯]
- 서브플롯 씬이 이 비트에 포함되어야 하는지 씬 플랜을 확인하라.
- 서브플롯 씬은 메인플롯의 테마를 다른 각도에서 비춰야 한다.
- 서브플롯 인물은 주인공의 거짓 신념에 도전하는 역할이다.
- 서브플롯이 메인플롯의 리듬을 깨뜨리지 않게 배치하라 (긴장 후 이완, 또는 대조).
- Midpoint(Beat 8) 또는 클라이맥스(Beat 13) 근처에서 서브플롯과 메인플롯이 충돌해야 한다.

[기능적 조연 — 세계를 살리는 인물들]
- 씬 플랜에 기능적 조연이 배치되어 있으면 반드시 살려라. 이름, 외형 1줄, 행동만 있으면 된다.
- 씬 플랜에 없더라도, 이 비트에서 아래 역할이 필요하면 자연스럽게 추가하라:
  · 정보를 흘리는 사람 (주인공이 직접 알 수 없는 것을 전달)
  · 주인공의 선택에 다른 관점을 보여주는 사람 ("나라면 안 그래")
  · 사건의 대가를 체감시키는 사람 (피해자, 목격자)
  · 긴장을 잠시 풀어주는 사람 (유머, 이완, 감초)
- 기능적 조연은 바이블이 필요 없다. 이름(성+직함/별명) + 나이 + 한 줄 외형 + 행동.
  예: 구급대원 박(30대, 지친 얼굴)이 들것을 밀며 고개를 젓는다.
- ★ 주요 캐릭터만으로 모든 씬을 채우면 세계가 비어 보인다. 조연이 세계를 살린다.

[관객 심리 설계]
- Dramatic Irony: 관객에게 캐릭터가 모르는 정보를 먼저 줘라. 관객이 더 많이 알수록 긴장이 올라간다.
- Information Gap: 이 비트에서 최소 1개의 "열린 질문"을 유지하라. 질문을 던지고 바로 답하지 마라.
- Zeigarnik: 비트 끝에 미해결 질문 또는 중단된 행동을 남겨라. 완결짓지 마라.
- Pattern & Violation: 반복되는 패턴이 있다면, 이 비트에서 깨뜨릴 타이밍인지 점검하라.
- Delayed Gratification: 관객이 원하는 정보/만남/대결을 최대한 늦춰라.

[집필 규칙]
10. 캐릭터 바이블의 말투·대사 샘플 참조 → 보이스 구분.
11. desire → obstacle → conflict → turn → emotional shift → exit pressure
12. 대사 = 행동. 서브텍스트 필수. 설명성 대사 금지.
13. 직전 비트와의 연속성 유지.
14. 지문(Action Line):
   - 한 단락 최대 3~4줄. 소설처럼 한 동작씩 줄바꿈 금지.
   - 카메라가 보는 것만. 내면 설명 금지.
   - 핵심 이미지 하나에 집중. "그는 ~한다. 그는 ~한다." 반복 금지.
   - 지문은 화면 재생 시간만큼만 (pithy).
15. 감정 연쇄 — 이 씬의 감정 톤이 다음 씬의 전제. 3%라도 빗나가면 안 된다.
16. Intention & Obstacle — 매 씬에서 "누가 뭘 원하고, 뭐가 막는가" 명확히.
17. "Too Wet" 금지 — 관객이 느낄 감정을 캐릭터가 직접 수행하지 마라.
18. AI ESCAPE — 집필 후 A1~A10 패턴 자가 점검:
   감정 설명 지문(A1) / 같은 말투(A2) / 본 것 반복(A3) / 무대 연출(A4) /
   편의적 정보(A5) / 침묵 부재(A6) / 대사 대칭(A7) / 처음부터 시작(A8) /
   같은 씬 해소(A9) / 총칭적 감각(A10) — 1개라도 해당되면 해당 부분을 다시 써라.

[OUTPUT]
맨 위에 아래 형식의 헤더를 반드시 먼저 출력하라:

═══════════════════════════════════════
{beat_info.get('act', '')} — Beat {beat_number}. {beat_info.get('name', '')}
═══════════════════════════════════════

그 다음 씬들을 순서대로 집필. 씬 사이 빈 줄 2개.
마지막에 --- 후 내부 메모:
- 비트 요약 1줄
- ⭐ 비트 구조 유형: [INV]/[CON]/[REV]/[EMO]/[ACT]/[SIL] 중 택 1
- ⭐ 액션 아이디어 전진: 이 비트에서 "___하는 일"에 어떻게 가까워졌는가 (전진/방해/무관)
- ⭐ 서사동력: Goal 추구 상태 / Need 인식 상태 / 간극 (벌어지는 중/좁혀지는 중)
- 작동한 장르 장치 목록 (예: fear_anticipation, dread_pacing, false_relief)
- 핵심 요소 추적: 이 비트에서 등장한 맥거핀/비밀/핵심장소/모티프 목록
- Plant/Payoff: 이 비트에서 심은 것(plant) 또는 회수한 것(payoff) — 없으면 "없음"
- ⭐ 장르 드라이브 5점 체크:
  ① 정보 비대칭: 관객이 아는 것 vs 주인공이 아는 것 (현재 상태)
  ② 에스컬레이션: 직전 비트 대비 위협 ↑/→/↓
  ③ 적대자: 이 비트에서 한 구체적 행동 + 승/패 (빌런이 이겼는가?)
  ④ 타이머: 남은 시간/자원 (줄었는가?)
  ⑤ 장르 쾌감: 이 비트의 "손에 땀" 순간 1줄
- 서브플롯 진행 상태
- 관객 심리: 현재 유지 중인 열린 질문 / Dramatic Irony
- 보이스 점검 1줄
""".strip()


