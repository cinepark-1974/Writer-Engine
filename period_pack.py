def detect_period_from_locked(locked_text: str) -> list:
    """
    LOCKED 블록(기획의도·시대·사건 등)에서 시대 키워드를 스캔하여
    매칭되는 시대 리스트를 반환한다.
    
    Args:
        locked_text: LOCKED 블록 전체 텍스트 (또는 아이디어 텍스트)
    
    Returns:
        매칭된 시대 키 리스트 (매칭 점수 내림차순).
        예: ["일제강점기_후기", "해방정국"]
        매칭 없으면 빈 리스트 [].
    
    매칭 점수 계산:
        각 키워드 출현 시 +1점. 연도(4자리 숫자)는 +2점 (강한 신호).
    """
    if not locked_text or not isinstance(locked_text, str):
        return []
    
    text = locked_text
    scores = {}
    
    for period_key, keywords in PERIOD_KEYWORDS_MAP.items():
        score = 0
        for kw in keywords:
            if not kw:
                continue
            # 단순 포함 검사 (대소문자 무시는 한글이라 무의미, 영문만 해당)
            count = text.count(kw)
            if count > 0:
                # 연도(4자리)는 가중치 2배
                if kw.isdigit() and len(kw) == 4:
                    score += count * 2
                else:
                    score += count
        if score > 0:
            scores[period_key] = score
    
    # 점수 내림차순 정렬
    sorted_periods = sorted(scores.items(), key=lambda x: x[1], reverse=True)
    return [p[0] for p in sorted_periods]


def get_period_detection_report(locked_text: str) -> dict:
    """
    시대 감지 상세 리포트.
    디버그·로그용. 작가가 어떤 키워드로 매칭되었는지 확인 가능.
    
    Returns:
        {
            "detected_periods": [...],
            "scores": {period_key: score, ...},
            "matched_keywords": {period_key: [kw1, kw2, ...], ...}
        }
    """
    if not locked_text or not isinstance(locked_text, str):
        return {"detected_periods": [], "scores": {}, "matched_keywords": {}}
    
    text = locked_text
    scores = {}
    matched = {}
    
    for period_key, keywords in PERIOD_KEYWORDS_MAP.items():
        score = 0
        hits = []
        for kw in keywords:
            if not kw:
                continue
            count = text.count(kw)
            if count > 0:
                if kw.isdigit() and len(kw) == 4:
                    score += count * 2
                else:
                    score += count
                hits.append(f"{kw}({count})")
        if score > 0:
            scores[period_key] = score
            matched[period_key] = hits
    
    sorted_periods = sorted(scores.items(), key=lambda x: x[1], reverse=True)
    return {
        "detected_periods": [p[0] for p in sorted_periods],
        "scores": dict(sorted_periods),
        "matched_keywords": matched,
    }


# ═══════════════════════════════════════════════════════════════
# BUILD FUNCTIONS — 프롬프트 블록 생성
# ═══════════════════════════════════════════════════════════════

def _format_period_block(period_key: str) -> str:
    """단일 시대의 프롬프트 블록 생성."""
    if period_key not in PERIOD_PACK:
        return ""
    
    data = PERIOD_PACK[period_key]
    label = data["label"]
    ref_films = " / ".join(data.get("ref_films", []))
    fields = data.get("fields", {})
    
    lines = [
        f"━━━━━━━━━━━━━━━━━━━━━━━━━━━",
        f"[시대 디테일 — {label}]",
        f"━━━━━━━━━━━━━━━━━━━━━━━━━━━",
        f"★ 이 작품의 시대 배경이다. 모든 씬·대사·지문에서 아래 디테일을 구체적으로 활용하라. ★",
        f"★ 레퍼런스: {ref_films} ★",
        "",
    ]
    
    field_labels = {
        "복식": "【복식】",
        "관직_호칭": "【관직·호칭】",
        "주거_공간": "【주거·공간】",
        "통화_거래": "【통화·거래】",
        "음식_기호": "【음식·기호품】",
        "교통": "【교통】",
        "언어_결": "【언어의 결】",
        "사건_맥락": "【시대 사건·사회 맥락】",
        "금지_오류": "【작가 흔한 고증 오류 — 절대 금지】",
        "핵심_레퍼런스_씬": "【핵심 레퍼런스 씬】",
    }
    
    for field_key, label_text in field_labels.items():
        value = fields.get(field_key, "").strip()
        if value:
            lines.append(label_text)
            lines.append(value)
            lines.append("")
    
    return "\n".join(lines).strip()


def build_period_block(
    locked_text: str = "",
    period_keys: list = None,
    max_periods: int = 2,
) -> str:
    """
    시대 디테일 블록 생성 (Period Pack 주입).
    
    Args:
        locked_text: LOCKED 블록 텍스트. 이것으로 자동 감지.
        period_keys: 수동 지정 시대 키 리스트 (자동 감지 우회).
        max_periods: 최대 주입 시대 수 (B안 — 기본 2개).
    
    Returns:
        프롬프트에 삽입할 시대 디테일 블록. 감지 실패 시 빈 문자열.
    
    B안 (다중 감지 정책):
        - 최대 2개 Period까지만 주입 (기본값).
        - 3개 이상 감지되면 점수 상위 2개 선택.
        - 다중 주입 시 상단에 교차 전개 안내문 1줄 추가.
    """
    # 시대 결정
    if period_keys:
        # 수동 지정 우선
        selected = [pk for pk in period_keys if pk in PERIOD_PACK][:max_periods]
    else:
        # 자동 감지
        detected = detect_period_from_locked(locked_text)
        selected = detected[:max_periods]
    
    if not selected:
        return ""
    
    # 블록 생성
    blocks = [_format_period_block(pk) for pk in selected]
    blocks = [b for b in blocks if b]  # 빈 블록 제거
    
    if not blocks:
        return ""
    
    # 다중 시대 안내문
    header = ""
    if len(blocks) >= 2:
        header = f"""
━━━━━━━━━━━━━━━━━━━━━━━━━━━
[★ 다중 시대 교차 전개 작품 ★]
━━━━━━━━━━━━━━━━━━━━━━━━━━━
이 작품은 아래 {len(blocks)}개 시대가 교차 전개된다.
각 씬이 어느 시대에 속하는지 명확히 구분하고, 해당 시대의 디테일을 정확히 적용하라.
시대가 섞인 장면(예: 플래시백)에서는 한 씬 안에서도 시대 전환을 시각적·청각적으로 구분하라.

"""
    
    return header + "\n\n".join(blocks) + "\n"


def build_period_block_auto(locked_text: str) -> str:
    """
    편의 함수 — LOCKED 텍스트만 넣으면 자동 감지·주입.
    prompt.py에서 가장 자주 호출될 인터페이스.
    """
    return build_period_block(locked_text=locked_text, max_periods=2)


