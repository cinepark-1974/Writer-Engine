def detect_period_by_year(year: int) -> str:
    """
    단일 연도로 시대 감지.
    연도가 여러 시대에 걸쳐 있으면 먼저 매칭된 시대 반환.
    
    Args:
        year: 서기 연도 (예: 1945)
    
    Returns:
        시대 키. 매칭 없으면 "".
    """
    if not isinstance(year, int) or year < 1392 or year > 1999:
        return ""
    for period_key, data in PERIOD_PACK.items():
        start, end = data.get("year_range", (0, 0))
        if start <= year <= end:
            return period_key
    return ""


